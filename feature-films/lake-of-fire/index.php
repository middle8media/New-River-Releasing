			<h2>Synopsis</h2>
			<p>A poor country girl tries to rebuild her life after murdering the boy who date-raped her in this Southern Gothic tale straight from the heart of rural America. Blending murder and revenge with themes of redemption and grace, Swimming in a Lake of Fire weaves together the lives of three damaged characters, Lucy Speckle (brilliantly portrayed by newcomer Ariel Burke), just home from prison and ready to mend her ways; her alcoholic preacher father, Ray Speckle (portrayed by Billy Ingram); and a broken-hearted cowboy, Lester Hollis (portrayed by Chris Best). Inspired by Flannery O'Connor and the Coen Brothers' films, SLOF has been called a "Southern masterpiece" (Betty Austin-Ware), capturing the beauty of North Carolina and the simplicity of rural life in America, reminiscent of movies such as Black Snake Moan and Winter's Bone.</p>

			<h2>Director's Statement</h2>
			<p>This film came from my interest in Flannery O'Connor’s work and the Southern Gothic idiom as a way weaving together dark, violent themes with religious belief, and my love of bigger-than-life, grotesque characters such as Lucy Speckle, her alcoholic preacher father, Ray, and the broken and scarred rodeo cowboy, Lester, who was trampled by a bull as a teenager. I wanted to breathe life into those characters in a story filled with twists and turns. I think we accomplished that and more … a movie with touches of humor, grace and poetry.</p>

			<h2>"Location First, Write Later"</h2>
			<p>The twist ending of this film was written after finding a location that was perfect for it, even though “it” was a brand new ending, an example of a location inspiring story.</p>

				<h1>1. What is your background?</h1>
				<p>I grew up surrounded by movies and music. Records and tapes filled my room, while action adventure filled by mind. Put it this way, I can remember the first record I heard (Paul Simon - 50 Ways to Leave Your Lover), the first tape I bought (Fat Boys), the first film I saw (one of the early 80's Pink Panther films, but I got sad and left when I realized it wasn't a cartoon) and the first time my family went from renting a VCR to buying a VCR, that was a big deal. But it wasn't until college that I decide to pursue a career in the arts. First it was music. I spent 10 years writing, recording, performing & touring, a variety of music in a variety of bands. During that time I dabbled in video production and documentary filmmaking, but music was my main focus. Then a gradual shift started to occur, I started watching and studying more films, paying attention to the credits, like who directed and produced the films, I began writing screenplays, while filmming anything I could, learning something new on each production. I quickly realized that film was something I could do for a long long time. Fast forward many years later and my passion for film and the creative arts still fuel me. I have several short films and mini-documentaries under my belt, two feature films that changed my career, a few screenplay in progress, tons of hours clocked in the edit bay, and a desire to produce great hand-crafted films with a community of people doing what they were meant to do. My newest creative endeavor is the wonderful world of web design and development (I created this New River site) and the online culture and community in general. The internet is essential to the producing, marketing and distribution of films, my adopting it was a logically progression.</p>


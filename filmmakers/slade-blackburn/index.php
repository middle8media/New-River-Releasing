				<h1>1. What is your background?</h1>
				<p>The short answer: I’ve spent nearly ten years in the trenches as an independent short-film maker, actor, and musician/composer.</p>
				<p>The long answer: Born in the Union but raised in the Confederacy, my outsider status has allowed me a unique view on life that I incorporate into my writing and filmmaking. My work can be described as a wolf in sheep’s clothing. Sometimes it can be seen as a wolf in sheep boots and matching belt with a leopard-print ascot. Other times it can even be a wolf with a hi-top fade and flip-flops and a fake I.D. that says “Sheep”.  But most of the time it’s just a really cool wolf that hangs out with the sheep. No disguise, no pretension, just a really chill wolf...but a wolf nonetheless.</p>

				<p>I have had an innate desire to be in film since as far back as I can remember. When I was five years old I began drawing posters for my "movies”. My most memorable was “Time Pilots”, the story of a group of army guys that traveled through time in their time-traveling army vehicles…or something like that.  I was five years old. Things like “character development”, “three-act structure”, and “plot” were not my strong suits. Although, drawing pictures of space helicopters and flying tanks and lasers were. The “Time Pilots” movie posters were followed by my first “screenplay” written at the age of six. It was titled “Star Devil” and it was about knights and kings in space…or something like that.  Basically everything I wrote during my “rookie season” was essentially Star Wars and G.I. Joe filtered through the mind of a weird little kid.</p>  

				<p>After graduating high school, I did what all aspiring filmmakers do: I enlisted in the United States Air Force. I soon found out the path to individualism, expression and Hollywood was not lined with fighter jets and C-130s. In 2004, my brother Ian and I decided to make a go of the whole film-thing and signed up to participate in our first “48 Hour Film Project” in Greensboro, NC. We decided the best way to approach it was to simply jump in completely blind. Actually, we decided nothing. That’s just how it happened. Lesson learned.</p>

				<p>In 2007 Ian and I created a semi-successful adult-themed odd-couple short film series with puppets titled “Roomies”. “Roomies” was about Mike, a NASCAR-loving good ol’ boy, and Raul, an earnest Mexican, sharing a house together.  The short films garnered several awards and thousands of views and eventually drew the attention of the Speed Channel.</p> 

				<p>In between working on original screenplays and the occasional acting gig, I work for the Speed Channel’s NASCAR show “Trackside” filming comedy segments with my puppet Mike, renamed “Danny Hammerdropper”.</p>

